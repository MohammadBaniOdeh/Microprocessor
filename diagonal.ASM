CODE	SEGMENT
	ASSUME	CS:CODE,DS:CODE,ES:CODE,SS:CODE
	;
PPIC_C	EQU	1FH
PPIC	EQU	1DH
PPIB	EQU	1BH
PPIA	EQU	19H	
KEY	EQU	01H
	;	
	ORG	1000H
	 MOV	AL,10000000B
	OUT	PPIC_C,AL
	MOV	AL,11110000B
	OUT	PPIB,AL
	;
	MOV	AL,00000000B
	OUT	PPIC,AL
	XOR	AX,AX
	MOV	DS,AX
	;

 L3: CALL SCAN
  CMP AL,00000101B
  JE L2
  JMP L3
  L2: 	MOV SI,OFFSET Data

L1:	
MOV AL,BYTE PTR CS:[SI]
         CMP  AL,00H
          JE L2
	OUT PPIA,AL
	CALL TIMER
	INC SI
      IN	AL,KEY
	TEST	AL,10000000B
	JNZ      L1
	TEST	AL,00010000B
        JNZ       L1
	;
	AND	AL,00001111B
 CMP AL,00H
 JE L3
	; key clear
	OUT	KEY,AL
	RET
	JMP L1
	
	
        ;
	INT 3
        ;





        		
TIMER:	MOV	CX,0
TIMER1:	NOP
	NOP
	NOP
	NOP
	LOOP	TIMER1
	RET
	

        ; key board scan, key code = AL
SCAN:	IN	AL,KEY
	TEST	AL,10000000B
	JNZ	SCAN
	TEST	AL,00010000B
	JNZ	SCAN
	;
	AND	AL,00001111B
	MOV	BYTE PTR K_BUF,AL
	; key clear
	OUT	KEY,AL
	RET
	;
	;
DATA:	DB	11000000B
	DB	11111001B
	DB	10100100B
	DB	10110000B
	DB	10011001B
	DB	10010010B
	DB	10000010B
	DB	11111000B
	DB	00H
	
K_BUF:	DB   1
tg:	
CODE	ENDS
	END
